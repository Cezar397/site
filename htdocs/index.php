						<h3>Al Doilea Război Mondial</h3>
						<br>
						<p class="lead">Se consideră că originile celui de-al Doilea Război Mondial datează încă din Primul Război Mondial (1914-1918). În acel războl Germania, condusă de ultranaționalistul Kaiser Wilhelm II, alături de aliații săi, a fost învinsă de o combinație între Regatul Unit, Statele Unite ale Americii, Franța, Rusia și alții. </p>
						<img class="rounded float-right" src="public/img/pic_hitler.jpg" oncontextmenu="return false">

						<p class="lead">Învingătorii au blamat în mod direct naționalismul militant al Germaniei Kaiserului (Împăratului); chiar Germania a fost cea care a început efectiv războiul cu un atac asupra Franței prin Belgia.</p>
						<p class="lead">Franța, care suferise anterior o înfrângere la mâna Prusiei (stat care s-a contopit un an mai târziu cu alții pentru a forma Germania), în războiul franco-prusac, în 1870, a căutat răzbunare pentru distrugerea sa pe plan financiar din timpul Primului Război Mondial (și umilirea în războiul anterior) și a asigurat că diversele tratate de pace, în special Tratatul de la Versailles, impuneau reparații financiare și restricții dure asupra Germaniei.</p>
						<br><br><br>
						<h3>O nouă republică</h3>
						<br>
						<p class="lead">A luat naștere o nouă republică democrată germană, cunoscută sub numele de Republica de la Weimar. După o perioadă de succes a fost lovită de hiperinflație și alte probleme economice grave.</p>
						<p class="lead">Elementele naționaliste de dreapta aflate într-o varietate de mișcări, dar mai ales Partidul Nazist al lui Adolf Hitler, au fost menite să învinovățească statutul "umilitor" al Germaniei în legătură cu asprimea așezării postbelice, slăbiciunea guvernării democratice și evreii, despre care se pretindea că posedau un monopol financiar asupra Germaniei. </p>
						<p class="lead">Hitler a fost numit Reichskanzler (cancelar) la 30 ianuarie 1933 de către bătrânul președinte von Hindenburg.</p>
						<p class="lead">Guvernul lui Hitler și-a exercitat o mare parte din putere prin intermediul puterilor speciale de urgență deținute de președinte în cadrul constituției.</p>
