									<h1 align="center">Tehnologii</h1>
									<br><br>
									<p class="lead">
									Acum o sută de ani, cu o jumătate de secol înainte de bombardarea atomică de la Hiroshima și Nagasaki, descoperirea razelor X a pus în lumină promisiunea extraordinară și periculul atomului.  Din acel moment și până în 1942, cercetarea atomică a fost privata.</p>
									<p class="lead">
									După ce s a sfârșit războiului, rețeaua de cercetători în domeniul radiațiilor, oficialități guvernamentale, militare și medici mobilizați pentru Proiectul Manhattan nu s-a desființat. Mai degrabă, au început să lucreze la programele guvernamentale pentru a promova atât utilizarea pașnică a energiei atomice, cât și dezvoltarea armelor nucleare. </p>
									<p class="lead">
										Probabilitatea aparentă ca bombele atomice să fie folosite din nou în război și că civilii americani, precum și soldații, sa fi e ținte, a însemnat că țara trebuia să știe cât de mult si cât de repede posibil despre efectele radiațiilor și  tratamentul leziunilor prin radiații.
									</p>
									<p class="lead">
										Această nevoie de cunoștințe a pus cercetătorii din domeniul radiațiilor, inclusiv medicii, în mijlocul noilor întrebări de risc și beneficiu, dezvăluire și consimțământ. Aceste întrebări au fost, în mod direct și indirect, un pericol fără precedent pentru sănătatea publică: războiul nuclear. În abordarea acestor întrebări, cercetătorii medicali au trebuit să definească noile roluri pe care le vor juca.
									</p>
									<p class="lead">
										În calitate de consilieri ai guvernului, cercetătorii de radiații au fost solicitați să asiste comandanții militari, care au solicitat experimentarea umană pentru a determina efectele armelor atomice asupra corpurilor lor. Dar acești cercetători au știut, de asemenea, că experimentarea umană nu poate oferi cu ușurință răspunsurile de care aveau nevoie militarii.
									</p>
								
								
